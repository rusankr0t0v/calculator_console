"""
Модуль, содержащий функции с математическими операциями
"""


def add(a, b):
    """
    Функция сложения.
    :param a: часло А
    :param b: Число Б
    :return: Результат сложения
    """
    return a + b


def subtract(a, b):
    """
    Функция вычитания.
    :param a: часло А
    :param b: Число Б
    :return: Результат вычитания
    """
    return a - b


def multiply(a, b):
    """
    Функция умножения.
    :param a: часло А
    :param b: Число Б
    :return: Результат умножения
    """
    return a * b


def divide(a, b):
    """
    Функция деления.
    :param a: часло А
    :param b: Число Б
    :return: Результат деления
    """
    return a / b